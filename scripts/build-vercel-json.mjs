/*
 * Narrative for the generated vercel.json (Vercel's schema rejects
 * _comment as an unknown top-level property, so the docs live here
 * instead of in the emitted JSON):
 *
 * GENERATED by scripts/build-vercel-json.mjs — do not edit vercel.json
 * by hand. Update the template inline in this script, or the route
 * manifest in scripts/seo-routes.mjs, and re-run `pnpm build`.
 *
 * Routing model:
 *   - Static files in /public (sitemap.xml, robots.txt, llms.txt,
 *     manifest.json, morphs.csv) ship as-is.
 *   - Every known SPA path is rewritten to /index.html; React Router
 *     handles it client-side and scripts/prerender.mjs overwrites
 *     /index.html per route at build time with route-aware metadata.
 *   - A catch-all `/(.*)` rewrite at the end of the rewrites array
 *     hands any unknown path to the SPA too, so full-page reloads on
 *     dynamic or newly-added routes return the React shell instead of
 *     a server 404. PageNotFound.jsx renders the 404 UX and emits a
 *     noindex meta for JS-executing crawlers. We previously shipped a
 *     static /public/404.html for a "real HTTP 404" SEO signal, but
 *     Vercel's filesystem step serves that file BEFORE the catch-all
 *     rewrite fires, breaking refresh on every non-prerendered route
 *     (Dashboard, MyGeckos, Settings, etc.). Removed 2026-04-21.
 *
 * Headers:
 *   - HTML is never cached at the CDN edge so a deploy rolls out immediately.
 *   - /assets/* is long-lived (content-hashed by Vite) so browsers/CDNs cache it.
 *   - Security headers follow the 2026 OWASP baseline.
 */
function buildConfig() {
  // 2026-04-21 debugging: the 147-entry enumerated rewrite list was
  // emitted correctly but Vercel serves a 404 for every
  // non-prerendered path (/MyGeckos, /Dashboard, /Settings, etc.)
  // including random strings that should hit the catch-all. Stripping
  // down to just the catch-all to see if the volume / ordering / other
  // config fields were somehow suppressing rewrite matches. If this
  // works, we'll rebuild the enumerated list; if it doesn't, the issue
  // is elsewhere (framework preset override, a project-level Install
  // / Build command, etc.).
  const rewrites = [{ source: '/(.*)', destination: '/index.html' }];

  // NOTE: Do NOT add `_comment` (or any unknown top-level key) to the
  // returned object. Vercel's schema validator now rejects unknown
  // properties with "should NOT have additional property X", which
  // fails the deploy before it even starts. The previous hand-rolled
  // vercel.json used `_comment` for inline docs and broke prod deploys
  // for ~2 hours before the pattern was removed. Keep all design notes
  // in this file and in scripts/seo-routes.mjs instead.
  return {
    $schema: 'https://openapi.vercel.sh/vercel.json',
    cleanUrls: true,
    trailingSlash: false,
    rewrites,
  };
}

function main() {
  const config = buildConfig();
  const body = JSON.stringify(config, null, 2) + '\n';
  writeFileSync(OUT_PATH, body, 'utf8');
  const count = config.rewrites.length;
  console.log(`[vercel.json] wrote ${count} SPA rewrites`);
}

main();
